package com.mobileboom.bean;

public class TipoUsuario {

	private Integer codigoTipoUsuario;
	private String descripcíon;
	
	public TipoUsuario(){};
	
	public Integer getCodigoTipoUsuario() {
		return codigoTipoUsuario;
	}
	public void setCodigoTipoUsuario(Integer codigoTipoUsuario) {
		this.codigoTipoUsuario = codigoTipoUsuario;
	}
	public String getDescripcíon() {
		return descripcíon;
	}
	public void setDescripcíon(String descripcíon) {
		this.descripcíon = descripcíon;
	}
	
	
}
